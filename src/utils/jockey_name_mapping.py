def _normalize_jockey_key(jockey_name: str) -> str:
    name = (jockey_name or "").strip().upper()
    # Remove apprentice marker and honorific noise.
    name = re.sub(r"\s*\(A\)\s*$", "", name)
    name = re.sub(r"\b(?:MISS|MS|MR|MRS|SIR)\b|[.]", "", name).strip()
    name = re.sub(r"\s+", " ", name)
    return name


def get_jockey_full_name(jockey_name: str) -> str:
    key = _normalize_jockey_key(jockey_name)
    return jockey_name_mapping.get(key, key)

